
CREATE TABLE ACTOR(ACT_ID INT PRIMARY KEY,ACT_NAME VARCHAR(20),ACT_GENDER CHAR(1));

DESC ACTOR;

CREATE TABLE DIRECTOR(DIR_ID INT PRIMARY KEY,DIR_NAME VARCHAR(20),DIR_PHONE INT);

DESC DIRECTOR;

CREATE TABLE MOVIES(MOV_ID INT PRIMARY KEY,MOV_TITLE VARCHAR(25),MOV_YEAR INT,MOV_LANG VARCHAR(12),DIR_ID INT REFERENCES DIRECTOR(DIR_ID));

DESC MOVIES;

CREATE TABLE MOVIECAST(ACT_ID INT REFERENCES ACTOR(ACT_ID),MOV_ID INT REFERENCES MOVIES(MOV_ID),ROLE VARCHAR(20));

DESC MOVIECAST;

CREATE TABLE RATING(MOV_ID INT REFERENCES MOVIES(MOV_ID) ,REV_STARS VARCHAR(25));

DESC RATING;

